/**
 * Get popular token addresses for a given chain
 * Returns up to 10 most popular tokens for the chain
 */
export const getPopularTokens = (chainId: CHAIN_IDS): string[] => {
  const tokens = POPULAR_TOKENS[chainId];
  if (!tokens || tokens.length === 0) {
    throw new Error(`No popular tokens configured for chainId: ${chainId}`);
  }
  return tokens.slice(0, 10); // Return first 10 tokens
};

/**
 * Get chain name by chainId
 * Returns human-readable chain name
 */
export const getChainName = (chainId: CHAIN_IDS | number): string => {
  const chainIdNum = typeof chainId === 'number' ? chainId : chainId;

  switch (chainIdNum) {
    case CHAIN_IDS.ETHEREUM:
      return 'Ethereum';
    case CHAIN_IDS.ARBITRUM:
      return 'Arbitrum';
    case CHAIN_IDS.BSC:
      return 'BSC';
    case CHAIN_IDS.POLYGON:
      return 'Polygon';
    case CHAIN_IDS.OPTIMISM:
      return 'Optimism';
    case CHAIN_IDS.BASE:
      return 'Base';
    case CHAIN_IDS.MANTLE:
      return 'Mantle';
    case CHAIN_IDS.METIS:
      return 'Metis';
    case CHAIN_IDS.SEI:
      return 'Sei';
    case CHAIN_IDS.LINEA:
      return 'Linea';
    case CHAIN_IDS.AVALANCHE:
      return 'Avalanche';
    case CHAIN_IDS.BERACHAIN:
      return 'Berachain';
    case CHAIN_IDS.SCROLL:
      return 'Scroll';
    case CHAIN_IDS.SONIC:
      return 'Sonic';
    case CHAIN_IDS.UNICHAIN:
      return 'Unichain';
    default:
      return `Chain ${chainIdNum}`;
  }
};

